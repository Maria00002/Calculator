function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}


console.log(add(13,16)); //29
console.log(subtract(13,16)); //-3
console.log(multiply(13,16)); //208
console.log(divide(13,16)); //0.8125

